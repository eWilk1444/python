# This file shows the different math symbols and their outputs

# Addition
a = 1 + 1
print("1 + 1 =", a)

# Subtraction
b = 1 - 1
print("1 - 1 =", b)

# Multiplication
c = 7 * 3
print("7 * 3 =", c)

# Division
d = 27 / 3
print("27 / 3 =", d)

# Integer Division (rounds down to nearest whole number)
e = 8 // 3
print("8 // 3 =", e)

# Modulus (remainder after equal division)
f = 15 % 4
print("15 % 4 =", f)

# Exponentiation
g = 2 ** 5
print("2 ** 5 =", g)
